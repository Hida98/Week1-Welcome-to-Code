console.log("LOOPING PERTAMA");

let i = 2; // mulai dari 2
while (i <= 20) {
  // selama i masih kurang/sama dengan 20
  console.log(i + " - I love coding");
  i += 2; // tambahkan 2 (supaya maju: 2, 4, 6, ... 20)
}

console.log("LOOPING KEDUA");

let j = 20; // mulai dari 20
while (j >= 2) {
  // selama j masih lebih besar/sama dengan 2
  console.log(j + " - I will become a web developer");
  j -= 2; // kurangi 2 (supaya mundur: 20, 18, ... 2)
}
